export const s = (object) => {
  return Object.keys(object)
    .map((k) => {
      const [key, value] = [k, object[k]];
      if (CssProperty[key]) return `${CssProperty[key]}: ${value}`;
      return `${k}: ${value}`;
    })
    .join(";");
};
